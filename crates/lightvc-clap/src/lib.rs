// Inference thread
// ---------------------------------------------------------------------------

fn inference_thread(
    task_rx: Receiver<Task>,
    metrics_tx: Sender<Metrics>,
    pipeline_ready: Arc<AtomicBool>,
    metrics: Arc<Mutex<Metrics>>,
) {
    let mut pipeline: Option<Arc<Mutex<lightvc_core::pipeline::VcPipeline>>> = None;
    let mut capture_rx: Option<rtrb::Consumer<f32>> = None;
    let mut playback_tx: Option<rtrb::Producer<f32>> = None;

    // Resampling state ([06-5]). Currently unused — reserved for the
    // future host-sample-rate resampling path.
    #[allow(unused_variables, unused_assignments)]
    let mut host_sr: u32 = 44_100;
    let mut resampler: Option<ClapResampler> = None;

    // 3-stage accumulation buffers ([06-5], mirroring realtime_tab.rs [05-4]).
    let mut in_accum: Vec<f32> = Vec::new(); // host-SR samples from capture ring
    let mut pcm_44k_accum: Vec<f32> = Vec::new(); // 44.1k samples ready for VC
    let mut out_44k_accum: Vec<f32> = Vec::new(); // 44.1k VC output
    let mut out_host_accum: Vec<f32> = Vec::new(); // host-SR output ready for playback

    loop {
        while let Ok(task) = task_rx.try_recv() {
            match task {
                Task::SetRingBuffers {
                    capture_rx: crx,
                    playback_tx: ptx,
                } => {
                    capture_rx = Some(crx);
                    playback_tx = Some(ptx);
                }
                Task::LoadModels {
                    dac_path,
                    converter_path,
                    config_path,
                } => match load_pipeline(&dac_path, &converter_path, &config_path) {
                    Ok((p, info)) => {
                        pipeline = Some(Arc::new(Mutex::new(p)));
                        pipeline_ready.store(true, Ordering::Relaxed);
                        let mut m = metrics.lock().unwrap();
                        m.pipeline_ready = true;
                        m.config_source = info.source;
                        m.config_summary = info.summary;
                        drop(m);
                    }
                    Err(e) => {
                        nice_log!("Model load failed: {e}");
                        pipeline_ready.store(false, Ordering::Relaxed);
                    }
                },
                Task::SetMode(val) => {
                    if let Some(p) = &pipeline {
                        let mode = match val {
                            0 => lightvc_core::converter::LatencyMode::Strict,
                            2 => lightvc_core::converter::LatencyMode::Quality,
                            _ => lightvc_core::converter::LatencyMode::Balanced,
                        };
                        if let Ok(mut pl) = p.lock() {
                            pl.set_mode(mode);
                            // Mode change resets streaming state; clear buffers.
                            in_accum.clear();
                            pcm_44k_accum.clear();
                            out_44k_accum.clear();
                            out_host_accum.clear();
                            nice_log!("Mode switched: {mode:?}");
                        }
                    }
                }
                Task::SetSampleRate(sr) => {
                    #[allow(unused_assignments)]
                    {
                        host_sr = sr;
                    }
                    if sr != 44_100 {
                        match ClapResampler::new(sr as usize, 2048) {
                            Ok(r) => {
                                resampler = Some(r);
                                nice_log!("Resampler active: {sr} Hz ↔ 44100 Hz");
                            }
                            Err(e) => {
                                nice_log!("Resampler init failed ({e}), assuming 44100 Hz");
                                resampler = None;
                            }
                        }
                    } else {
                        resampler = None;
                        nice_log!("Host SR = 44100 Hz, no resampling needed");
                    }
                    // Clear all buffers on SR change.
                    in_accum.clear();
                    pcm_44k_accum.clear();
                    out_44k_accum.clear();
                    out_host_accum.clear();
                }
            }
        }

        let (Some(p), Some(crx), Some(ptx)) = (&pipeline, &mut capture_rx, &mut playback_tx) else {
            std::thread::sleep(std::time::Duration::from_millis(50));
            continue;
        };

        let chunk_sz = p.lock().map(|pl| pl.chunk_samples()).unwrap_or(2048);

        // ---- Stage 1: drain capture ring into in_accum ----
        while let Ok(s) = crx.pop() {
            in_accum.push(s);
        }

        // ---- Stage 2: resample host-SR → 44.1k (or bypass) ----
        if let Some(rsm) = &mut resampler {
            let needed_up = rsm.input_frames_needed_up();
            while in_accum.len() >= needed_up {
                let (chunk, rest) = in_accum.split_at(needed_up);
                match rsm.process_up(chunk) {
                    Ok(out44) => pcm_44k_accum.extend_from_slice(out44),
                    Err(e) => nice_log!("process_up: {e}"),
                }
                in_accum = rest.to_vec();
            }
        } else {
            // No resampling: move directly.
            pcm_44k_accum.append(&mut in_accum);
        }

        // ---- Stage 3: process VC chunks at 44.1k ----
        let mut did_process = false;
        let mut in_rms = 0.0f32;
        let mut process_elapsed = std::time::Duration::ZERO;
        let mut out_samples_total = 0usize;

        while pcm_44k_accum.len() >= chunk_sz {
            let (chunk, rest) = pcm_44k_accum.split_at(chunk_sz);
            let chunk_owned = chunk.to_vec();
            pcm_44k_accum = rest.to_vec();

            in_rms = rms(&chunk_owned);
            let t0 = Instant::now();
            let out = match p.lock() {
                Ok(mut pl) => pl.process_chunk(&chunk_owned).unwrap_or_else(|e| {
                    nice_log!("VC: {e}");
                    chunk_owned.clone()
                }),
                Err(_) => break,
            };
            process_elapsed += t0.elapsed();
            out_samples_total += out.len();
            out_44k_accum.extend_from_slice(&out);
            did_process = true;
        }

        // ---- Stage 4: resample 44.1k → host-SR (or bypass) → playback ----
        if let Some(rsm) = &mut resampler {
            let needed_down = rsm.input_frames_needed_down();
            while out_44k_accum.len() >= needed_down {
                let (chunk, rest) = out_44k_accum.split_at(needed_down);
                match rsm.process_down(chunk) {
                    Ok(out_host) => {
                        for &s in out_host {
                            let _ = ptx.push(s);
                        }
                    }
                    Err(e) => nice_log!("process_down: {e}"),
                }
                out_44k_accum = rest.to_vec();
            }
        } else {
            for &s in &out_44k_accum {
                let _ = ptx.push(s);
            }
            out_44k_accum.clear();
        }

        // ---- Metrics ----
        if did_process {
            let out_rms_val = if out_samples_total > 0 {
                // Approximate: use last chunk's output via the accumulator tail.
                let tail_start = out_host_accum.len().saturating_sub(out_samples_total);
                rms(&out_host_accum[tail_start.min(out_host_accum.len())..])
            } else {
                0.0
            };
            let audio_dur_s = out_samples_total as f32 / 44_100.0;
            let rtf = if audio_dur_s > 0.0 {
                process_elapsed.as_secs_f32() / audio_dur_s
            } else {
                0.0
            };
            // Latency estimate: algorithmic + cpal buffers + resample ([06-8]).
            let algo_ms = p
                .lock()
                .map(|pl| pl.algorithmic_latency_ms())
                .unwrap_or(0.0);
            let latency_ms = 10.0 + 3.0 + algo_ms + 3.0 + 10.0;
            let (cfg_src, cfg_sum) = {
                let m = metrics.lock().unwrap();
                (m.config_source.clone(), m.config_summary.clone())
            };
            let _ = metrics_tx.send(Metrics {
                input_rms: in_rms,
                output_rms: out_rms_val,
                rtf,
                pipeline_ready: true,
                latency_ms,
                config_source: cfg_src,
                config_summary: cfg_sum,
            });
        }

        if !did_process {
            std::thread::sleep(std::time::Duration::from_millis(2));
        }
    }
}

fn rms(samples: &[f32]) -> f32 {
    if samples.is_empty() {
        return 0.0;
    }
    let sum_sq: f32 = samples.iter().map(|s| s * s).sum();
    (sum_sq / samples.len() as f32).sqrt()
}

/// Resolved converter configuration + provenance, for UI feedback.
struct ConverterConfigInfo {
    /// "explicit" | "sidecar" | "default"
    source: String,
    /// Short human-readable summary, e.g. "flow, hidden=256, timbre=off".
    summary: String,
}

/// Load converter config from, in order:
///   1. explicit `config_path` JSON (if set and readable)
///   2. `{converter_path without extension}_config.json` sidecar (written by
///      `training/export_weights.py`)
///   3. `ConverterConfig::default()` (legacy single-variant fallback)
fn load_converter_config(
    converter_path: &str,
    config_path: &str,
) -> (lightvc_core::ConverterConfig, ConverterConfigInfo) {
    let try_read_json = |p: &str| -> Option<lightvc_core::ConverterConfig> {
        let text = std::fs::read_to_string(p).ok()?;
        serde_json::from_str(&text).ok()
    };

    // 1. explicit config path
    if !config_path.is_empty() {
        if let Some(cfg) = try_read_json(config_path) {
            let summary = summarize_config(&cfg);
            return (
                cfg,
                ConverterConfigInfo {
                    source: "explicit".into(),
                    summary,
                },
            );
        }
        nice_log!("config_path set but unreadable: {config_path}, falling back");
    }

    // 2. sidecar next to the model file
    let sidecar = sidecar_config_path(converter_path);
    if let Some(cfg) = try_read_json(&sidecar) {
        let summary = summarize_config(&cfg);
        return (
            cfg,
            ConverterConfigInfo {
                source: "sidecar".into(),
                summary,
            },
        );
    }

    // 3. default
    let cfg = lightvc_core::ConverterConfig::default();
    let summary = summarize_config(&cfg);
    (
        cfg,
        ConverterConfigInfo {
            source: "default".into(),
            summary,
        },
    )
}

fn sidecar_config_path(converter_path: &str) -> String {
    // Strip the file extension and append `_config.json`.
    // `Path::with_extension("")` correctly handles dots inside directory names.
    let base = std::path::Path::new(converter_path).with_extension("");
    format!("{}_config.json", base.to_string_lossy())
}

fn summarize_config(cfg: &lightvc_core::ConverterConfig) -> String {
    format!(
        "{}, hidden={}, timbre={}",
        cfg.model_type, cfg.hidden_dim, cfg.enable_timbre
    )
}

fn load_pipeline(
    dac_path: &str,
    converter_path: &str,
    config_path: &str,
) -> anyhow::Result<(lightvc_core::pipeline::VcPipeline, ConverterConfigInfo)> {
    let device = candle_core::Device::Cpu;
    let dac_config = lightvc_core::DacConfig::default();

    let vb = lightvc_core::weights::load_varbuilder(
        std::path::Path::new(converter_path),
        candle_core::DType::F32,
        &device,
    )?;
    let (config, info) = load_converter_config(converter_path, config_path);
    nice_log!("Converter config [{}]: {}", info.source, info.summary);
    let converter = lightvc_core::converter::AnyConverter::new(config, vb)?;

    let pipeline = lightvc_core::pipeline::VcPipeline::new(
        std::path::Path::new(dac_path),
        &dac_config,
        converter,
        lightvc_core::converter::LatencyMode::Balanced,
        device,
    )?;
    Ok((pipeline, info))
}

