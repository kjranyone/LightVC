//! LightVC VST3 Plugin
//!
//! Real-time voice conversion as a VST3 audio effect.
//! Uses nice-plug + nice-plug-egui.

use std::sync::atomic::{AtomicBool, Ordering};
use std::sync::{Arc, Mutex};
use std::time::Instant;

use crossbeam_channel::{unbounded, Receiver, Sender};
use nice_plug::prelude::*;
use nice_plug_egui::{create_egui_editor, EguiState};

// ---------------------------------------------------------------------------
// Parameters
// ---------------------------------------------------------------------------

#[derive(Params)]
struct LightVcParams {
    #[id = "bypass"]
    pub bypass: BoolParam,

    #[id = "mode"]
    pub mode: IntParam,

    #[id = "mix"]
    pub mix: FloatParam,

    #[id = "gain"]
    pub output_gain: FloatParam,

    #[persist = "model-path"]
    pub model_path: Arc<Mutex<String>>,

    #[persist = "dac-path"]
    pub dac_path: Arc<Mutex<String>>,

    /// Optional explicit converter config JSON path. If empty, the plugin
    /// looks for `{model_basename}_config.json` next to the model file
    /// (generated by `training/export_weights.py`). Falls back to
    /// `ConverterConfig::default()` if neither exists.
    #[persist = "config-path"]
    pub config_path: Arc<Mutex<String>>,

    #[persist = "editor-state"]
    pub editor_state: Arc<EguiState>,
}

impl Default for LightVcParams {
    fn default() -> Self {
        Self {
            bypass: BoolParam::new("Bypass", false),
            mode: IntParam::new("Mode", 1, IntRange::Linear { min: 0, max: 2 }),
            mix: FloatParam::new(
                "Mix",
                100.0,
                FloatRange::Linear {
                    min: 0.0,
                    max: 100.0,
                },
            )
            .with_smoother(SmoothingStyle::Linear(50.0))
            .with_unit("%"),
            output_gain: FloatParam::new(
                "Output",
                0.0,
                FloatRange::Skewed {
                    min: -24.0,
                    max: 24.0,
                    factor: FloatRange::gain_skew_factor(-24.0, 24.0),
                },
            )
            .with_smoother(SmoothingStyle::Logarithmic(20.0))
            .with_unit(" dB"),
            model_path: Arc::new(Mutex::new(String::new())),
            dac_path: Arc::new(Mutex::new(String::new())),
            config_path: Arc::new(Mutex::new(String::new())),
            editor_state: EguiState::from_size(400, 300),
        }
    }
}

// ---------------------------------------------------------------------------
// Communication
// ---------------------------------------------------------------------------

#[derive(Clone, Debug, Default)]
struct Metrics {
    input_rms: f32,
    output_rms: f32,
    rtf: f32,
    pipeline_ready: bool,
    /// Algorithmic + buffer latency estimate in ms ([06-8]).
    latency_ms: f32,
    /// "default" | "explicit" | "sidecar" — how the converter config was loaded.
    config_source: String,
    /// Human-readable summary of the active converter config.
    config_summary: String,
}

enum Task {
    LoadModels {
        dac_path: String,
        converter_path: String,
        config_path: String,
    },
    SetRingBuffers {
        capture_rx: rtrb::Consumer<f32>,
        playback_tx: rtrb::Producer<f32>,
    },
    /// Switch latency/quality mode ([06-6]). 0=Strict, 1=Balanced, 2=Quality.
    SetMode(u8),
    /// Set host sample rate for resampling ([06-5]).
    SetSampleRate(u32),
}

// ---------------------------------------------------------------------------
// Plugin
// ---------------------------------------------------------------------------

struct LightVcPlugin {
    params: Arc<LightVcParams>,
    task_tx: Sender<Task>,
    metrics_rx: Receiver<Metrics>,
    pipeline_ready: Arc<AtomicBool>,
    metrics: Arc<Mutex<Metrics>>,
    // Ring buffer handles for process() (host audio thread)
    capture_tx: Option<rtrb::Producer<f32>>,
    playback_rx: Option<rtrb::Consumer<f32>>,
    // Latency in samples (reported to host)
    latency_samples: u32,
    /// Last mode value sent to inference thread ([06-6]).
    last_mode: u8,
}

impl Default for LightVcPlugin {
    fn default() -> Self {
        let (task_tx, task_rx) = unbounded();
        let (metrics_tx, metrics_rx) = unbounded();
        let pipeline_ready = Arc::new(AtomicBool::new(false));
        let metrics = Arc::new(Mutex::new(Metrics::default()));

        let pr = pipeline_ready.clone();
        let mt = metrics.clone();

        std::thread::spawn(move || {
            inference_thread(task_rx, metrics_tx, pr, mt);
        });

        Self {
            params: Arc::new(LightVcParams::default()),
            task_tx,
            metrics_rx,
            pipeline_ready,
            metrics,
            capture_tx: None,
            playback_rx: None,
            latency_samples: 0,
            last_mode: 1, // Balanced
        }
    }
}

